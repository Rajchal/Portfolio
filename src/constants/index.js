export const move = (x, y, z, po1, po2, po3) => {
  if (x < po1) {
    x += 0.01;
  } else if (x > po2) {
    x -= 0.01;
  }

  if (y < po2) {
    y += 0.01;
  } else if (y > po2) {
    y -= 0.01;
  }

  if (z < po3) {
    z += 0.01;
  } else if (z > po3) {
    z -= 0.01;
  }

  return [x, y, z];
};
